        # –ü–æ–ª—É—á–∞–µ–º –∫—É—Ä—Å –≤–∞–ª—é—Ç—ã, –µ—Å–ª–∏ –Ω–µ RUB
        rate = 1.0 # –¥–ª—è RUB
        if currency in ["EUR", "USD"]:
            rate = await get_currency_rate(currency)
            if rate is None:
                await callback.message.edit_text("–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –∫—É—Ä—Å–∞ –≤–∞–ª—é—Ç—ã. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –ø–æ–∑–∂–µ.")
                await callback.answer()
