    currency = callback.data.split("_")[1] # —Ä–∞–∑–¥–µ–ª—è–µ—Ç callback_data –∏ –±–µ—Ä–µ—Ç –≤—Ç–æ—Ä–æ–π —ç–ª–µ–º–µ–Ω—Ç
