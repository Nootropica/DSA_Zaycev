# –ò–Ω–∏—Ü–∏–∞–ª–∏–∑–∞—Ü–∏—è –ë–î
def init_db():
    conn = get_db_connection()
    cursor = conn.cursor()

    # –°–æ–∑–¥–∞–Ω–∏–µ —Ç–∞–±–ª–∏—Ü
    cursor.execute('''
        CREATE TABLE IF NOT EXISTS users (
            id SERIAL PRIMARY KEY,
            name VARCHAR(255) NOT NULL,
            chat_id BIGINT UNIQUE NOT NULL,
            date DATE NOT NULL DEFAULT CURRENT_DATE
        )
    ''')

    cursor.execute('''
        CREATE TABLE IF NOT EXISTS operations (
            id SERIAL PRIMARY KEY,
            date DATE NOT NULL,
            sum DECIMAL(10, 2) NOT NULL,
            chat_id BIGINT NOT NULL,
            type_operation VARCHAR(10) NOT NULL CHECK (type_operation IN ('–î–û–•–û–î', '–†–ê–°–•–û–î')),
            FOREIGN KEY (chat_id) REFERENCES users(chat_id) ON DELETE CASCADE
        )
    ''')

    conn.commit()
    cursor.close()
    conn.close()


# –ü—Ä–æ–≤–µ—Ä–∫–∞ —Ä–µ–≥–∏—Å—Ç—Ä–∞—Ü–∏–∏ –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è
def is_user_registered(chat_id: int) -> bool:
    conn = get_db_connection()
    cursor = conn.cursor()

    cursor.execute(
        "SELECT EXISTS(SELECT 1 FROM users WHERE chat_id = %s)",
        (chat_id,)
    )
    result = cursor.fetchone()[0]

    cursor.close()
    conn.close()
    return result


# –ü–æ–ª—É—á–µ–Ω–∏–µ –∫—É—Ä—Å–∞ –≤–∞–ª—é—Ç—ã
async def get_currency_rate(currency: str) -> Optional[float]:
    try:
        async with aiohttp.ClientSession() as session:
            async with session.get(f"{CURRENCY_SERVICE_URL}?currency={currency}") as response:
                if response.status == 200:
                    data = await response.json()
                    return data.get('rate')
                return None
    except Exception as e:
        logging.error(f"–û—à–∏–±–∫–∞ –ø–æ–ª—É—á–µ–Ω–∏—è –∫—É—Ä—Å–∞ –≤–∞–ª—é—Ç—ã: {e}")
        return None


